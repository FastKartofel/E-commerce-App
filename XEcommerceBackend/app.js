var createError = require('http-errors');
var express = require('express');
const cors = require('cors');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');
/* FOR CONNECTING WITH THE MSSQL DATABASE(for now not working, issues with the database login) */
const sql = require('mssql');
const dbConfig = require('./dbconfig');
/* CONNECTING TO THE MONGODB DATABASE*/
const mongoose = require('mongoose');

/* here I am adding the route to the GET method*/
const users = require('./routes/usersEndpoint');
const products = require('./routes/productsEndpoint');

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(logger('dev'));
app.use(cors()); // Enable CORS
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/api/users', users);
app.use('/api/products', products);

/* CONNECTING TO THE MONGODB DATABASE */
mongoose.connect('mongodb://localhost:27017/EcommerceData', {
  useNewUrlParser: true,
  useUnifiedTopology: true
});

const db = mongoose.connection;
db.on('error', console.error.bind(console, 'MongoDB connection error:'));
db.once('open', function() {
  console.log("Connected successfully to MongoDB");
});

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;